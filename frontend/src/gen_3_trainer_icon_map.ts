const ICONS_PATH = 'trainer_overworld'

export function get_trainer_icon(trainer: Trainer): string {
  const id_name = trainer.idName

  if (direct[id_name]) {
    return `${ICONS_PATH}/${direct[id_name].toLowerCase()}.png`
  }

  // Direct name mapping remove all numbers
  const id_name_clean = id_name.replace(/_[0-9]/g, '').toLowerCase()
  if (ICONS.includes(id_name_clean)) {
    return `${ICONS_PATH}/${id_name_clean}.png`
  }

  if (id_name_clean === `gabby_and_ty`) {
    return `${ICONS_PATH}/cameraman.png`
  }

  if (id_name.includes(`TABITHA`)) {
    return `${ICONS_PATH}/magma_member_m.png`
  }

  if (id_name.includes(`WALLY`)) {
    return `${ICONS_PATH}/wally.png`
  }

  if (id_name.includes(`BRENDAN`)) {
    return `${ICONS_PATH}/brendan_walking.png`
  }

  if (id_name.includes(`MAY`)) {
    return `${ICONS_PATH}/may_walking.png`
  }

  /*
    "ARCHIE", "DUDLEY", "COLE", "JEFF", "AXLE", "JACE", "KEEGAN", "VICTOR", "VICTORIA", "VICKY", "CALVIN_1", "CALVIN_2", "CALVIN_3", "CALVIN_4", "CALVIN_5", "ALLEN", "KAYLEE", "GRUNT_UNUSED", "TERRY", "TIANA", "RICK", "MIKE_1"]
  */
  // Not handled But I don't care

  return `${ICONS_PATH}/anabel.png`
}
